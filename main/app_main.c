#include <stdio.h>
#include <string.h>

#include "esp_log.h"
#include "freertos/FreeRTOS.h"
#include "freertos/task.h"
#include "initialisation.h"

/////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////// DECLARATION DES VARIABLES STATIC
/////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////
void app_main(void) {
  ESP_LOGI("main", "Init DSP & Queues");

  initialisation();

  // Création des tâches
  xTaskCreate(led_task, "led_task", 2048, NULL, 1, NULL);
  xTaskCreate(acquisition_task, "acquisition_task", 8192, NULL, 5, NULL);
  xTaskCreate(processing_task, "processing_task", 8192, NULL, 5, NULL);
  xTaskCreate(output_task, "output_task", 8192, NULL, 5, NULL);
}
